function optionsEstadosBR(estadosBR) {
    const estados =  document.querySelector('#estados');
    for (const key in estadosBR) {
        let criando = document.createElement('option');
        criando.value = key;
        criando.innerText = estadosBR[key];
        estados.appendChild(criando);
    }
}

optionsEstadosBR(estadosBR);